  computed: {},
  watch: {},
  methods: {},
  created () {},
  mounted () {
    // 左右滚动
    $('#right').click(function () {
      $('.box-lh-one ul').stop().animate({
        left: '-297px'
      })
      $(this).css({
        color: '#ccc'
      })
      $('#left').css({
        color: '#000'
      })
    })
    $('#left').click(function () {
      $('.box-lh-one ul').stop().animate({
        left: 0
      })
      $(this).css({
        color: '#ccc'
      })
      $('#right').css({
        color: '#000'
      })
    })

    // 换图片
    $('.box-lh-one li').mouseover(function () {
      $(this)
        .css({
          padding: '0',
          border: 'solid 1px red'
        })
        .siblings()
        .css({
          padding: '1px',
          border: 'none'
        })
    })

    function Zoom (imgbox, hoverbox, l, t, x, y, tw, hh, showbox) {
      var moveX = x - l - tw / 2
      // 鼠标区域距离
      var moveY = y - t - hh / 2
      // 鼠标区域距离
      if (moveX < 0) {
        moveX = 0
      }
      if (moveY < 0) {
        moveY = 0
      }
      if (moveX > imgbox.width() - tw) {
        moveX = imgbox.width() - tw
      }
      if (moveY > imgbox.height() - hh) {
        moveY = imgbox.height() - hh
      }
      // 判断鼠标使其不跑出图片框
      var zoomX = showbox.width() / imgbox.width()
      // 求图片比例
      var zoomY = showbox.height() / imgbox.height()

      showbox.css({
        left: -(moveX * zoomX),
        top: -(moveY * zoomY)
      })
      hoverbox.css({
        left: moveX,
        top: moveY
      })
      // 确定位置
    }
    function Zoomhover (imgbox, hoverbox, showbox) {
      var l = imgbox.offset().left
      var t = imgbox.offset().top
      var w = hoverbox.width()
      var h = hoverbox.height()
      $('.probox img,.hoverbox')
        .mouseover(function (e) {
          var x = e.pageX
          var y = e.pageY
          $('.hoverbox,.showbox').show()
          hoverbox.css('opacity', '0.3')
          setTimeout(function () {
            Zoom(imgbox, hoverbox, l, t, x, y, w, h, showbox)
          }, 1)
        })
        .mousemove(function (e) {
          var x = e.pageX
          var y = e.pageY
          setTimeout(function () {
            Zoom(imgbox, hoverbox, l, t, x, y, w, h, showbox)
          }, 1)
        })
        .mouseout(function () {
          showbox.parent().hide()
          hoverbox.hide()
        })
    }
    $(function () {
      Zoomhover($('.probox img'), $('.hoverbox'), $('.showbox img'))
      $('.box-lh-one ul li').hover(function () {
        $('.img1').attr('src', $(this).find('img').attr('src'))
      })
    })
  },
  beforeCreate () {},
  beforeMount () {},
  beforeUpdate () {},
  updated () {},
  beforeDestroy () {},
  destroyed () {},
  activated () {}
}
